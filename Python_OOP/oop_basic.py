class People:
    def __init__(self, name , age, height):
        self.people_name = name
        self.people_age = age
        self.people_height = height



people_1 = People("Rahim", "27", "59")

print(people_1.people_name)
print(people_1.people_age)
print(people_1.people_height)

people_2 = People("Karim", "37", "56")

print(people_2.people_name)
print(people_2.people_age)
print(people_2.people_height)